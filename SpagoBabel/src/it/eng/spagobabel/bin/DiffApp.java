package it.eng.spagobabel.bin;

import java.io.File;
import java.io.FileOutputStream;
import java.io.IOException;
import java.io.PrintWriter;
import java.util.Map;
import java.util.Properties;
import java.util.Set;

import it.eng.spagobabel.diff.DiffCalculator;
import it.eng.spagobabel.io.JSBoundleReader;
import it.eng.spagobabel.io.KeySetFileReader;

/**
 * @author Andrea Gioia (andrea.gioia@eng.it)
 */
public class DiffApp extends AbstractApp {
	
	
	
	private static final String stringRegExp = "'[^']+'";
	public static final String TARGET_LANGUAGE = "fr";
	public static final String REFERENCE_LANGUAGE = "en";
	
	public static void main(String[] args) throws IOException {
		File inputFile, diffDir, diffFile, boundlesDir, referenceBoundleFile, targetBoundleFile;
		PrintWriter diffWriter;
		String targetLanguage, referenceLanguage;
		
			
		boundlesDir = new File(getArg(args, 0, BOUNDLES_DIR));
		targetLanguage = getArg(args, 1, TARGET_LANGUAGE);
		referenceLanguage = getArg(args, 2, REFERENCE_LANGUAGE);
		inputFile = new File(getArg(args, 3, LABELS_FILE));
		diffDir = new File(getArg(args, 4, OUTPUT_DIR));
		
		diffFile = new File(diffDir, targetLanguage + "-diff.properties");
		
		JSBoundleReader boundleReader = new JSBoundleReader();
				
		referenceBoundleFile = new File(boundlesDir,  referenceLanguage + ".js");
		targetBoundleFile = new File(boundlesDir,  targetLanguage + ".js");
		
		Map targetLabelsMap = boundleReader.read(targetBoundleFile);
		Map referenceLabelsMap = boundleReader.read(referenceBoundleFile);
		
		
		KeySetFileReader keysReader = new KeySetFileReader();
		Set<String> keys = keysReader.read(inputFile);
		
		double translated = 0;
		
	
		DiffCalculator diffCalculator = new DiffCalculator();
		Properties diffMap = diffCalculator.getDiff(keys, targetLabelsMap, referenceLabelsMap);
		
		diffMap.store(new FileOutputStream(diffFile), "diff file for language [" + targetLanguage + "] generated by SpagoBabel on ...");
		
		
		translated = keys.size() - diffMap.keySet().size();
		
		System.out.println(translated + "/" + keys.size());
		System.out.println(Math.floor((translated / keys.size())*100) + "%");
	}

}
